	}

	/**
	 * ScaffoldBlockThemeAbility honours the overwrite flag.
	 */
	public function test_scaffold_overwrites_when_flag_set(): void {
		$slug    = $this->unique_slug( 'overwrite-ok' );
		$ability = new ScaffoldBlockThemeAbility( 'sd-ai-agent/scaffold-block-theme' );

		$first = $ability->run(
			[
				'slug' => $slug,
				'name' => 'First',
			]
		);
		$this->assertIsArray( $first );

		$second = $ability->run(
			[
				'slug'      => $slug,
				'name'      => 'Second',
