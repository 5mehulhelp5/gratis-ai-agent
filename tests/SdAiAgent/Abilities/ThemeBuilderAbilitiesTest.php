/**
 * Test case for ThemeBuilderAbilities (ScaffoldBlockThemeAbility + ActivateThemeAbility).
 *
 * Covers t226c subphase 3a (GH#1385): the two abilities that underpin the
 * theme-builder onboarding branch. Tests are structured around the WP_Error
 * branches each ability exposes so the AI agent gets a usable signal on
