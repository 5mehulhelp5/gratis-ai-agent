namespace SdAiAgent\Tests\Abilities;

use SdAiAgent\Abilities\ActivateThemeAbility;
use SdAiAgent\Abilities\ScaffoldBlockThemeAbility;
use WP_UnitTestCase;

/**
 * Test ThemeBuilderAbilities functionality.
 */
class ThemeBuilderAbilitiesTest extends WP_UnitTestCase {

	/**
	 * Slugs created during tests, removed in tearDown.
	 *
	 * @var array<int,string>
	 */
	private array $created_slugs = [];

	/**
	 * Original active stylesheet, restored in tearDown.
	 *
	 * @var string
	 */
	private string $original_stylesheet = '';

	public function setUp(): void {
		parent::setUp();
		$this->original_stylesheet = (string) get_stylesheet();
	}

	public function tearDown(): void {
		// Restore the original stylesheet so other tests are not affected by
		// switch_theme() side effects from this test class.
		if ( '' !== $this->original_stylesheet
			&& $this->original_stylesheet !== get_stylesheet()
		) {
			switch_theme( $this->original_stylesheet );
		}

		foreach ( $this->created_slugs as $slug ) {
			$dir = trailingslashit( get_theme_root() ) . $slug;
			if ( is_dir( $dir ) ) {
				self::rrmdir( $dir );
			}
		}

		parent::tearDown();
	}

