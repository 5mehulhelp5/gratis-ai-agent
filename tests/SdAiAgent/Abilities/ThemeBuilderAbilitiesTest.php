	/**
	 * ScaffoldBlockThemeAbility creates the three mandatory files plus the
	 * minimum templates/index.html and returns a well-formed result.
	 */
	public function test_scaffold_creates_block_theme_skeleton(): void {
		$slug    = $this->unique_slug( 'scaffold-happy' );
		$ability = new ScaffoldBlockThemeAbility( 'sd-ai-agent/scaffold-block-theme' );

		$result = $ability->run(
			[
				'slug'        => $slug,
				'name'        => 'Scaffold Happy',
				'description' => 'Generated by ThemeBuilderAbilitiesTest.',
				'author'      => 'AI Agent',
			]
		);

		$this->assertIsArray( $result );
		$this->assertSame( $slug, $result['slug'] );
		$this->assertSame( $slug, $result['stylesheet'] );
		$this->assertFalse( $result['overwritten'] );

		$theme_dir = trailingslashit( get_theme_root() ) . $slug;
		$this->assertDirectoryExists( $theme_dir );
		$this->assertFileExists( $theme_dir . '/theme.json' );
		$this->assertFileExists( $theme_dir . '/style.css' );
		$this->assertFileExists( $theme_dir . '/functions.php' );
		$this->assertFileExists( $theme_dir . '/templates/index.html' );

		// theme.json must be valid JSON.
		$theme_json = json_decode( (string) file_get_contents( $theme_dir . '/theme.json' ), true );
		$this->assertIsArray( $theme_json );
		$this->assertSame( 3, $theme_json['version'] );

		// style.css must carry the canonical Theme Name header so wp_get_theme
		// can detect it.
		$style_css = (string) file_get_contents( $theme_dir . '/style.css' );
		$this->assertStringContainsString( 'Theme Name: Scaffold Happy', $style_css );
		$this->assertStringContainsString( 'Text Domain: ' . $slug, $style_css );

		// functions.php must declare ABSPATH guard and prefixed setup function.
		$functions_php = (string) file_get_contents( $theme_dir . '/functions.php' );
		$this->assertStringContainsString( "if ( ! defined( 'ABSPATH' ) )", $functions_php );
		$prefix = str_replace( '-', '_', $slug );
		$this->assertStringContainsString( "function {$prefix}_setup()", $functions_php );
	}

	/**
	 * ScaffoldBlockThemeAbility refuses to overwrite by default.
